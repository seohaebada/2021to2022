List<Int>? : 리스트에 null이 들어갈 수 없다. 리스트 자체는 null 일 수 있다.
List<Int?>? : 리스트에 null이 들어갈 수도 있고, 리스트 자체가 null 일 수 있다.

JAVA는 읽기 전용 컬렉션 - 변경 가능 컬렉션을 구분하지 않는다.
